import Foundation
import UIKit

extension UIColor {
    @nonobjc class var turkishSea: UIColor {
        return UIColor(red: 25 / 255.0, green: 81 / 255.0, blue: 144 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var veryRed: UIColor {
        // 186 0 32
        return UIColor(red: 186 / 255.0, green: 0 / 255.0, blue: 32 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var grayBlack: UIColor {
        // 33 35 34
        return UIColor(red: 33 / 255.0, green: 35 / 255.0, blue: 34 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var coolGray: UIColor {
        // 136 139 141
        return UIColor(red: 136 / 255.0, green: 139 / 255.0, blue: 141 / 255.0, alpha: 1.0)
    }
}
